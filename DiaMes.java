public class DiaMes {
	
	public String getMes (int d){

		if (d == 0){

			return "Janeiro";
		}

		if (d == 1){

			return "Fevereiro";
		}

		if (d == 2){

			return "Marco";
		}

		if (d == 3){

			return "Abril";
		}

		if (d == 4){

			return "Maio";
		}

		if (d == 5){

			return "Junho";
		}

		if (d == 6){

			return "Julho";
		}

		if (d == 7){

			return "Agosto";
		}

		if (d == 8){

			return "Setembro";
		}

		if (d == 9){

			return "Outubro";
		}

		if (d == 10){

			return "Novembro";
		}

		if (d == 11){

			return "Dezembro";
		}

		return "Error";
	}
}